package com.rgb.beans;

public class AccountType {
	 
	Integer SavingAccount;
	Integer CurrentAccount;
	
	public Integer getSavingAccount() {
		return SavingAccount;
	}
	public void setSavingAccount(Integer savingAccount) {
		SavingAccount = savingAccount;
	}
	public Integer getCurrentAccount() {
		return CurrentAccount;
	}
	public void setCurrentAccount(Integer currentAccount) {
		CurrentAccount = currentAccount;
	}
	
	

}
